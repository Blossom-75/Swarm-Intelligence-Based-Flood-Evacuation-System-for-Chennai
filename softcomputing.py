def get_edge_cost(G, u, v):
    edges = G.get_edge_data(u, v)
    min_cost = float("inf")
    for key in edges:
        edge = edges[key]
        cost = edge["length"] + 10 * edge["flood"]
        if cost < min_cost:
            min_cost = cost
    return min_cost

# SECTION 4: GENETIC ALGORITHM ROUTE

def ga_route(G, start, end, population_size=50, generations=40, mutation_rate=0.2):
    """
    Genetic Algorithm for flood-aware evacuation routing.
    Each chromosome is a path from start to end.
    Fitness = inverse of total cost (length + flood penalty).
    """
    print("\n[GA] Initializing population via random walks + Dijkstra seeds...")

    try:
        dijkstra_seed = nx.shortest_path(G, start, end, weight="length")
    except:
        dijkstra_seed = None

    try:
        flood_aware_seed = nx.shortest_path(
            G, start, end,
            weight=lambda u, v, d: min(
                d2.get("length", 100) + 10 * d2.get("flood", 0)
                for d2 in G.get_edge_data(u, v).values()
            )
        )
    except:
        flood_aware_seed = dijkstra_seed

    def path_cost(path):
        if len(path) < 2:
            return float("inf")
        total = 0
        for u, v in zip(path[:-1], path[1:]):
            if G.has_edge(u, v):
                total += get_edge_cost(G, u, v)
            else:
                total += 1e9
        return total

    def random_walk_path(start, end, max_steps=1500):
        """Random walk that tries to reach end within max_steps."""
        path = [start]
        current = start
        visited = set([start])
        for _ in range(max_steps):
            neighbors = list(G.neighbors(current))
            if not neighbors:
                break
            unvisited = [n for n in neighbors if n not in visited]
            if not unvisited:
                unvisited = neighbors
            current = random.choice(unvisited)
            path.append(current)
            visited.add(current)
            if current == end:
                return path
        if path[-1] != end and dijkstra_seed:
            try:
                tail = nx.shortest_path(G, path[-1], end, weight="length")
                path = path + tail[1:]
            except:
                pass
        return path

    def crossover(parent1, parent2):
        """
        Order-based crossover: find common nodes, swap tails.
        """
        common = set(parent1) & set(parent2) - {start, end}
        if not common:
            return parent1[:]
        pivot = random.choice(list(common))
        if pivot in parent1 and pivot in parent2:
            idx1 = parent1.index(pivot)
            idx2 = parent2.index(pivot)
            child = parent1[:idx1 + 1] + parent2[idx2 + 1:]
            return child
        return parent1[:]

    def mutate(path):
        """
        Mutation: replace a sub-segment with a random walk or Dijkstra path.
        """
        if len(path) < 4:
            return path
        idx = random.randint(1, len(path) - 3)
        node = path[idx]
        try:
            new_tail = nx.shortest_path(G, node, end, weight="length")
            return path[:idx] + new_tail
        except:
            return path

    population = []
    if dijkstra_seed:
        population.append(dijkstra_seed[:])
    if flood_aware_seed:
        population.append(flood_aware_seed[:])

    while len(population) < population_size:
        p = random_walk_path(start, end)
        if p[-1] == end:
            population.append(p)

    while len(population) < population_size:
        population.append(dijkstra_seed[:] if dijkstra_seed else [start, end])

    best_path = None
    best_cost = float("inf")

    for gen in tqdm(range(generations), desc="Running GA"):
        costs = [path_cost(p) for p in population]
        fitness_scores = [1.0 / (c + 1e-6) for c in costs]
        total_fitness = sum(fitness_scores)

        min_cost_idx = np.argmin(costs)
        if costs[min_cost_idx] < best_cost:
            best_cost = costs[min_cost_idx]
            best_path = population[min_cost_idx][:]
            print(f"  ✓ GA new best at gen {gen}: cost={round(best_cost, 2)}, nodes={len(best_path)}")

        probs = [f / total_fitness for f in fitness_scores]
        selected_indices = np.random.choice(
            len(population), size=population_size, replace=True, p=probs
        )
        selected = [population[i] for i in selected_indices]

        new_population = []

        sorted_pop = sorted(zip(costs, population), key=lambda x: x[0])
        new_population.append(sorted_pop[0][1][:])
        new_population.append(sorted_pop[1][1][:])

        while len(new_population) < population_size:
            p1, p2 = random.sample(selected, 2)
            child = crossover(p1, p2)
            if random.random() < mutation_rate:
                child = mutate(child)
            if child[-1] != end:
                try:
                    tail = nx.shortest_path(G, child[-1], end, weight="length")
                    child = child + tail[1:]
                except:
                    child = dijkstra_seed[:] if dijkstra_seed else child
            new_population.append(child)

        population = new_population

    if best_path is None:
        print("[GA] Failed — using Dijkstra fallback")
        return dijkstra_seed if dijkstra_seed else [start]

    print(f"[GA] Best path: {len(best_path)} nodes, cost={round(best_cost, 2)}")
    return best_path

# SECTION 4: ACO ROUTE

def aco_route(G, start, end, ants=30, iterations=30):
    pheromone = {}
    for u, v in G.edges():
        pheromone[(u, v)] = 1.0
        pheromone[(v, u)] = 1.0

    best_path = None
    best_cost = float("inf")

    try:
        fallback = nx.shortest_path(G, start, end, weight="length")
    except:
        fallback = None

    for iteration in range(iterations):
        for ant in range(ants):
            current = start
            visited = set([current])
            path = []
            cost = 0
            max_steps = 2000
            steps = 0

            while current != end and steps < max_steps:
                steps += 1
                neighbors = [n for n in G.neighbors(current) if n not in visited]
                if not neighbors:
                    neighbors = list(G.neighbors(current))
                    if not neighbors:
                        break

                probabilities = []
                for n in neighbors:
                    tau = pheromone.get((current, n), 1.0)
                    edge_cost = get_edge_cost(G, current, n)
                    eta = 1.0 / (edge_cost + 1e-6)
                    probabilities.append((tau ** 1.0) * (eta ** 2.0))

                probabilities = np.array(probabilities)
                total = probabilities.sum()
                if total == 0:
                    probabilities = np.ones(len(probabilities)) / len(probabilities)
                else:
                    probabilities /= total

                if random.random() < 0.2 and fallback and current in fallback:
                    idx = fallback.index(current)
                    if idx + 1 < len(fallback):
                        next_node = fallback[idx + 1]
                    else:
                        next_node = np.random.choice(neighbors, p=probabilities)
                else:
                    next_node = np.random.choice(neighbors, p=probabilities)

                cost += get_edge_cost(G, current, next_node)
                path.append((current, next_node))
                visited.add(next_node)
                current = next_node

            if current == end and cost < best_cost:
                best_cost = cost
                best_path = path
                print(f"  ✓ ACO found path at iteration {iteration}, cost={round(best_cost,2)}, steps={len(best_path)}")

        for edge in pheromone:
            pheromone[edge] *= 0.9

        if best_path:
            for edge in best_path:
                pheromone[edge] = pheromone.get(edge, 1.0) + (1.0 / best_cost)

    if best_path is None:
        print("ACO failed — using Dijkstra fallback")
        return fallback if fallback else [start]

    route = [start]
    for edge in best_path:
        route.append(edge[1])
    return route

