# SECTION 3- PSO FOR FLOOD SEVERITY OPTIMIZATION

def fitness(weights):
    error = 0
    for u, v, k, data in G.edges(keys=True, data=True):
        predicted = (
            weights[0] * data["rainfall"]
            + weights[1] * data["elevation"]
            + weights[2] * data["drainage"]
            + weights[3] * data["soil"]
        )
        true_value = 0.5 * data["rainfall"] + 0.3 * data["elevation"]
        error += (predicted - true_value) ** 2
    return error

particles = 20
iterations = 25
dimensions = 4

positions = np.random.rand(particles, dimensions)
velocities = np.random.rand(particles, dimensions)

pbest = positions.copy()
pbest_scores = np.array([fitness(p) for p in positions])
gbest = pbest[np.argmin(pbest_scores)]

w, c1, c2 = 0.5, 1.5, 1.5

for _ in tqdm(range(iterations), desc="Running PSO"):
    for i in range(particles):
        velocities[i] = (
            w * velocities[i]
            + c1 * random.random() * (pbest[i] - positions[i])
            + c2 * random.random() * (gbest - positions[i])
        )
        positions[i] += velocities[i]
        score = fitness(positions[i])
        if score < pbest_scores[i]:
            pbest[i] = positions[i]
            pbest_scores[i] = score
    gbest = pbest[np.argmin(pbest_scores)]

print("Optimized Flood Weights:", gbest)

