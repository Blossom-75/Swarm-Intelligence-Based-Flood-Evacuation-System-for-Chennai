def compute_flood_score(route):
    if len(route) < 2:
        return float("inf")
    return sum(
        min(G[u][v][k]["flood"] for k in G[u][v])
        for u, v in zip(route[:-1], route[1:])
        if G.has_edge(u, v)
    )

